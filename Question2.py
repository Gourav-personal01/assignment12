# Data defination language (DDL) is used for using the opertion like creates, alters, deletes etc objects.

# 1. CREATE is used to create tables in the databse
#  Example- CREATE TABLE table_name {c1,c2,c3}

# 2. DROP is used to drop/remove existing table in the database
#  Example - DROP TABLE table_name;

# 3. ATLER is used to ADD, DELETE and Modify the Existing table.
# Example - ATLER TABLE table_name
#           ADD column1 datatype;

# 4. TRUNCATE is used to drop/remove all rows from existing table in the database.
# Example - TRUNCATE TABLE Categories;