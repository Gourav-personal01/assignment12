# DML Stands for Data manipulation Language which is used to manupulate the data inside a database.

# 1. INSERT is used to insert new records in the table
#  Example - INSERT INTO table_name
#            VALUES (value1, value2, value3, ...);

# 2. UPDATE is used to modify the existing record in the table
#  Example - UPDATE table_name
#            SET column1 = value1, column2 = value2, ...
#            WHERE condition;

# 3. DELETE is used to delete the existing record in the table
#  Example - DELETE FROM table_name WHERE condition;